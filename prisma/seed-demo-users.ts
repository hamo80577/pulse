export function assertDemoSeedAllowed(nodeEnv = process.env.NODE_ENV) {
  if (nodeEnv === "production") {
    throw new Error("Demo users seed cannot run in production.");
  }
}

export function buildDemoCredentialDocument(credentials: DemoCredential[]) {
  const lines = [
    "# Pulse Local Demo Credentials",
    "",
    "Generated by `npm run seed:demo-users`.",
    `This file is intentionally written only to \`${demoCredentialFilePath}\`.`,
    "",
    "| Role | Phone | Password |",
    "| --- | --- | --- |",
    ...credentials.map(
      (credential) =>
        `| ${credential.role} | ${credential.phone} | ${credential.password} |`,
    ),
    "",
    "All demo users are forced to change password on first login.",
    "",
  ];

  return lines.join("\n");
}

function createAlphanumericPassword() {
  let password = "";

  while (password.length < 10) {
    password += createSecretToken(12).replace(/[^a-zA-Z0-9]/g, "");
  }

  return password.slice(0, 10);
}

async function main() {
  assertDemoSeedAllowed();

  const [{ PrismaPg }, { PrismaClient }] = await Promise.all([
    import("@prisma/adapter-pg"),
    import("../generated/prisma/client"),
  ]);
  const adapter = new PrismaPg({
    connectionString:
      process.env.DATABASE_URL ??
      "postgresql://pulse_app:replace-with-a-local-password@localhost:5433/pulse_local?schema=public",
  });
  const prisma = new PrismaClient({ adapter });
  const credentials: DemoCredential[] = [];

  try {
    for (const demoUser of demoUsers) {
      const password = createAlphanumericPassword();
      const passwordHash = await hashPassword(password);
      const user = await prisma.user.upsert({
        where: { phone: demoUser.phone },
        update: {
          name: demoUser.name,
          email: demoUser.email,
          phone: demoUser.phone,
          role: demoUser.role,
          status: "ACTIVE",
          passwordHash,
          mustChangePassword: true,
          employeeProfile: {
            upsert: {
              create: demoUser.profile,
              update: demoUser.profile,
            },
          },
        },
        create: {
          name: demoUser.name,
          email: demoUser.email,
          phone: demoUser.phone,
          role: demoUser.role,
          status: "ACTIVE",
          passwordHash,
          mustChangePassword: true,
          employeeProfile: {
            create: demoUser.profile,
          },
        },
        select: { id: true, phone: true, role: true },
      });

      await prisma.auditLog.create({
        data: {
          actorUserId: null,
          action: "DEMO_USER_SEEDED",
          entityType: "User",
          entityId: user.id,
          newValueJson: { phone: user.phone, role: user.role },
        },
      });

      credentials.push({ phone: demoUser.phone, password, role: demoUser.role });
    }

    await writeFile(
      demoCredentialFilePath,
      buildDemoCredentialDocument(credentials),
      "utf8",
    );
    console.log(
      `Seeded ${credentials.length} demo users. Credentials were written to ${demoCredentialFilePath}.`,
    );
  } finally {
    await prisma.$disconnect();
  }
}

if (process.env.VITEST !== "true") {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
